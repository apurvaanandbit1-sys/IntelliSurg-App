import random

def get_demo_ecg_signal(class_name: str) -> str:
    """
    Returns a comma-separated string of 187 float values representing
    a real ECG signal for the requested class. Randomly selects from available samples.
    """
    if class_name in ECG_SAMPLES and len(ECG_SAMPLES[class_name]) > 0:
        signal_array = random.choice(ECG_SAMPLES[class_name])
        return ",".join([str(val) for val in signal_array])
    return ",".join(["0.0"] * 187)

